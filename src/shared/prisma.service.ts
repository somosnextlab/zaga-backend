import { Injectable, OnModuleInit, OnModuleDestroy } from '@nestjs/common';
import { ConfigService } from '@nestjs/config';
import { PrismaClient } from '@prisma/client';

@Injectable()
export class PrismaService
  extends PrismaClient
  implements OnModuleInit, OnModuleDestroy
{
  constructor(private configService: ConfigService) {
    const databaseUrl = configService.get<string>('DATABASE_URL');
    const isProduction = configService.get<string>('NODE_ENV') === 'production';
    
    super({
      datasources: {
        db: {
          url: isProduction && databaseUrl && !databaseUrl.includes('pgbouncer=true')
            ? `${databaseUrl}?pgbouncer=true`
            : databaseUrl,
        },
      },
      log:
        configService.get<string>('NODE_ENV') === 'development'
          ? ['query', 'info', 'warn', 'error']
          : ['error'],
    });
  }

  async onModuleInit() {
    try {
      await this.$connect();
      console.log('üìã PrismaService conectado a la base de datos');

      // Verificar conexi√≥n con una consulta simple (evitar prepared statements en producci√≥n)
      const isProduction = this.configService.get<string>('NODE_ENV') === 'production';
      if (isProduction) {
        // En producci√≥n, usar una consulta m√°s simple que no genere prepared statements
        await this.$executeRaw`SELECT 1`;
      } else {
        // En desarrollo, usar queryRaw para debugging
        await this.$queryRaw`SELECT 1`;
      }
      console.log('‚úÖ Conexi√≥n a base de datos verificada');
    } catch (error) {
      console.error('‚ùå Error conectando a la base de datos:', error);
      // En producci√≥n, no fallar si hay problemas con prepared statements
      const isProduction = this.configService.get<string>('NODE_ENV') === 'production';
      if (isProduction && error.code === 'P2010') {
        console.warn('‚ö†Ô∏è Advertencia: Problema con prepared statements, continuando sin verificaci√≥n');
        return;
      }
      throw error;
    }
  }

  async onModuleDestroy() {
    try {
      await this.$disconnect();
      console.log('üîå PrismaService desconectado de la base de datos');
    } catch (error) {
      console.error('‚ùå Error desconectando de la base de datos:', error);
    }
  }

  /**
   * Ejecuta una transacci√≥n con rollback autom√°tico en caso de error
   *
   * @param fn - Funci√≥n que contiene las operaciones de la transacci√≥n
   * @returns Promise con el resultado de la transacci√≥n
   */
  async executeTransaction<T>(
    fn: (prisma: PrismaClient) => Promise<T>,
  ): Promise<T> {
    return this.$transaction(fn);
  }

  /**
   * Verifica la salud de la conexi√≥n a la base de datos
   *
   * @returns Promise<boolean> - true si la conexi√≥n est√° saludable
   */
  async isHealthy(): Promise<boolean> {
    try {
      const isProduction = this.configService.get<string>('NODE_ENV') === 'production';
      if (isProduction) {
        // En producci√≥n, usar executeRaw para evitar prepared statements
        await this.$executeRaw`SELECT 1`;
      } else {
        // En desarrollo, usar queryRaw para debugging
        await this.$queryRaw`SELECT 1`;
      }
      return true;
    } catch (error) {
      console.error('‚ùå Base de datos no saludable:', error);
      return false;
    }
  }
}
